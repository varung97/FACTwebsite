<h1>Home</h1>

<p>This is a webpage that allows you to run consensus tree algorithms.</p>

<p>The algorithms on this website are described in the following papers:</p>

<UL><LI>
J. Jansson, C. Shen, and W.-K. Sung: "Improved Algorithms for Constructing Consensus Trees",
in Proceedings of the Twenty-Fourth Annual ACM-SIAM Symposium on Discrete Algorithms (SODA 2013),
pp. 1800-1813, SIAM, 2013.
</LI></UL>
<UL><LI>
J. Jansson, C. Shen, and W.-K. Sung:
"An Optimal Algorithm for Building the Majority Rule Consensus Tree",
in Proceedings of the Seventeenth Annual International Conference on
Research in Computational Molecular Biology (RECOMB 2013), LNCS, Vol. 7821,
pp. 88-99, Springer, 2013.
</LI></UL>
<UL><LI>
J. Jansson, C. Shen, and W.-K. Sung:
"Algorithms for the Majority Rule (+) Consensus Tree and the Frequency
Difference Consensus Tree",
to appear in Proceedings of the Thirteenth Workshop on Algorithms in
Bioinformatics (WABI 2013), LNCS, Springer, 2013.
</LI></UL>

<p>Prototype implementations in C++ can be downloaded from here:
<a href = 'source_code.zip'>FACT</a> (Fast Algorithms for Consensus Trees) and <a href = "https://github.com/Mesh89/FACT2">here</a> (Frequency Difference).</p>

<p>See also the <a href = 'http://www.siam.org/meetings/da13/'>SODA 2013</a> and
<a href = 'http://bioinfo.au.tsinghua.edu.cn/recomb2013/'>RECOMB 2013</a> websites.</p>
